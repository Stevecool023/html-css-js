/*
 * Logical operators combine or manipulate boolean values.
 */

// Logical operators
let isSunny = true;
let isWarm = false;

console.log('Logical AND; isSunny && isWarm:', isSunny && isWarm); // false
console.log('Logical OR; isSunny || isWarm:', isSunny || isWarm); // true
console.log('Logical NOT; !isSunny:', !isSunny); // false
