/*
 * Comparison Operators return boolean values based on the comparison of two values.
 */

// Comparison operators example
let x = 10;
let y = 5;

console.log('Comparing 10 to 5 respectively');
console.log('Greater than:', x > y); // true
console.log('Less than:', x < y); // false
console.log('Greater than/equal to:', x >= y); // true
console.log('Less than or equal to:', x <= y); // false
console.log('Equal to (strict):', x === y); //false
console.log('Not equal to (strict):', x !== y); // true
